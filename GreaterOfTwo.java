public class GreaterOfTwo {
    
    public static void main(String[] args) {
        int num1 = 80, num2 = 90;

        if(num1 > num2){
            System.out.println("Num1 is greater");
        }else if(num1 < num2){
            System.out.println("Num2 is greater");
        }else{
            System.out.println("Num1 and Num2 are equal");
        }
    }
}
